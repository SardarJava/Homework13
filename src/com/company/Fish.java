package com.company;

public class Fish {
    String name;
    String breed;
    int age;

    void setName (String name){
        this.name = name;}
    void setBreed (String breed){
        this.breed = breed;}
    void setAge (int age) {
        this.age = age;}

    String getName (){
        return name;
    }
    String getBreed (){
        return breed;
    }
    int getAge (){
        return age;
    }

}
