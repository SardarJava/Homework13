package com.company;

public class Student {
    private String name;
    private String surname;
    private String email;
    private int age;

    String getName (){
        return name;}
    String getSurname (){
        return surname;}
    String getEmail (){
        return email;}
    int getAge (){
        return age;}


    void setName (String name) {
        this.name = name;}
    void setSurname (String surname){
        this.surname = surname;}
    void setEmail (String email) {
        this.email = email;}
    void setAge (int age){
        this.age = age;}



}
